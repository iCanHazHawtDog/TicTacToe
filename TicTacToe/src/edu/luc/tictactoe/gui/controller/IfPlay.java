/**
 * If the user wants to play a network based game, this method is run.
 * We check to make sure that the inputs are valid, then proceed to the
 * network based windows/forms
 * 
 * @param evt
 */
private void nbBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_nbBtnMouseReleased
    //This is where the network based stuff is going to be handled at this point
	
	String player1NameString= player1Name.getText();
	if(!(player1NameString.isEmpty())){
	
		MainApplication.ticTacToePlay = new TicTacToePlay(GameType.WithAnotherPersonInNetwork);
    	MainApplication.ticTacToePlay.setPlayerOne(player1NameString);
    	
		IfNetworkBased ifNetworkBased= new IfNetworkBased();
	    JFrame mainFrame= TicTacToeUIApp.getApplication().getMainFrame();
	    ifNetworkBased.setLocationRelativeTo(mainFrame);
	    this.dispose();
	    TicTacToeUIApp.getApplication().show(ifNetworkBased);
	    ifNetworkBased.setSize(300,280);
	}
    
}//GEN-LAST:event_nbBtnMouseReleased

