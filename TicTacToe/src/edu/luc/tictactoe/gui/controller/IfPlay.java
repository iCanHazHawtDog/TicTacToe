/**
 * If the user wishes to play against another user on this machine, the following method is run.
 * We first check to make sure that the input is not blank, then we go ahead and proceed to show
 * the next windows for the host based game.
 * 
 * @param evt
 */
private void otmBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_otmBtnMouseReleased
    //set the player2 name..
    //TO-DO Add code in here to put up a dialog box saying that the player name 
    //is invalid, please enter a new one.
	//TODO AKREM WE NEED TO INITIALIZE THE PLAYERS HERE for on this machine..
	
    String player1NameString= player1Name.getText();
    if(!(player1NameString.isEmpty())){
       
    	MainApplication.ticTacToePlay = new TicTacToePlay(GameType.WithAnotherPersonInSameComputer);
    	MainApplication.ticTacToePlay.setPlayerOne(player1NameString);
    	
       IfOTM ifOtm= new IfOTM();
       JFrame mainFrame = TicTacToeUIApp.getApplication().getMainFrame();
       ifOtm.setLocationRelativeTo(mainFrame);
       //TO-DO Figure out how to close this application window without exiting the whole application
       TicTacToeUIApp.getApplication().show(ifOtm);
       ifOtm.setSize(310,315);
       this.dispose();
    }
    
  
    
}//GEN-LAST:event_otmBtnMouseReleased

