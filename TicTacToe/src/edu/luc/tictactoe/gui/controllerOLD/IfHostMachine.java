/**
 * If the user wants to go back to the main menu this method is run
 * We close the window and interrupt the networking.
 * 
 * @param evt
 */
private void mainMenuBtnMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mainMenuBtnMouseReleased
	this.dispose();
    networkThread.interrupt();
    TicTacToeUIApp.getApplication().getMainFrame().setVisible(true);
}//GEN-LAST:event_mainMenuBtnMouseReleased

/**
 * Once the client has been connected, the user can play the game by selecting the "Play" button.
 * Selecting the Play button runs this method. 
 * 
 * This method creates a new game board, sets the size and allows the user to play the game.
 * @param evt
 */
private void playBtnMouseReleased(java.awt.event.MouseEvent evt) {
	
    mainBoard.setLocationRelativeTo(this);
    this.dispose();
    TicTacToeUIApp.getApplication().show(mainBoard);
    mainBoard.setResizable(false);
    mainBoard.setSize(815, 482);
	
}

